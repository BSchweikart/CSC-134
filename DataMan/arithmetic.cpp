int add (int num1, int num2)
{
	// take two int find sum return value
	int sum = num1 + num2;
	return sum;
}

int subtract(int,int)
{
	int sum = num1 - num2;
	return sum;
}
int multiply(int,int)
{
	int sum = num1 * num2;
	return sum;
}
float divide(float,float)
{
	int sum = num1 / num2;
	return sum;
}